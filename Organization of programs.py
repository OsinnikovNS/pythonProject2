# Практическая работа по уроку "Организация программ и методы строк"
# создаем переменную
my_car = 'Haval F7'
# вывод длинны строки
print('длинна строки:', len (my_car))
# вывод строки в верхнем регистре
print (my_car.upper())
# вывод строки в нижнем регистре
print (my_car.lower())
# Изменение строки my_car, удалив все пробелы.
my_car = my_car.replace(" ","")
print (my_car)
# Вывод первого символа строки.
print(my_car[0])
# Вывод последнего символа строки.
print(my_car[-1])